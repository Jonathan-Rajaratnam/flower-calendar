export const sampleFlowers = []

export function generateFlowersForMonth(year, month) {
  // Month is 0-indexed (0 = January, 11 = December)
  const daysInMonth = new Date(year, month + 1, 0).getDate()
  const monthStr = (month + 1).toString().padStart(2, '0')
  const yearStr = year.toString()
  const generatedFlowers = []

  // Check if there are already manually assigned flowers for this month
  const existingFlowers = sampleFlowers.filter((flower) => {
    if (!flower.date) return false
    return flower.date.startsWith(`${yearStr}-${monthStr}`)
  })

  // If we have existing flowers for this month, return them
  if (existingFlowers.length > 0) {
    return existingFlowers
  }

  // Calculate how many days have passed in the year
  const dayOfYear = (day) => {
    const startOfYear = new Date(year, 0, 0)
    const currentDay = new Date(year, month, day)
    const diff = (currentDay - startOfYear) / (1000 * 60 * 60 * 24)
    return diff
  }

  // Otherwise, assign flowers from library to each day of the month
  for (let day = 1; day <= daysInMonth; day++) {
    const dateStr = `${yearStr}-${monthStr}-${String(day).padStart(2, '0')}`

    // Use day-of-year to pick a flower, ensuring each day of the year gets a unique flower
    // We use modulo to cycle through the flower library if we have more days than flowers
    const dayNumber = dayOfYear(day)
    const flowerIndex = (dayNumber - 1) % flowerLibrary.length
    const flowerTemplate = flowerLibrary[flowerIndex]

    generatedFlowers.push({
      ...flowerTemplate,
      date: dateStr,
      id: `${yearStr}${monthStr}${String(day).padStart(2, '0')}`,
    })
  }

  return generatedFlowers
}

export function getFlowerByDate(dateString) {
  // First check if we have a manually specified flower for this date
  const manualFlower = sampleFlowers.find((flower) => flower.date === dateString)
  if (manualFlower) return manualFlower

  // If not, generate a flower
  if (!dateString) return null

  const [year, month, day] = dateString.split('-').map((num) => parseInt(num, 10))
  const monthFlowers = generateFlowersForMonth(year, month - 1)

  return monthFlowers.find((flower) => flower.date === dateString)
}

export function getFlowersForMonth(year, month) {
  // First check if we have manually specified flowers for this month
  const monthStr = (month + 1).toString().padStart(2, '0')
  const yearStr = year.toString()

  const manualFlowers = sampleFlowers.filter((flower) => {
    if (!flower.date) return false
    return flower.date.startsWith(`${yearStr}-${monthStr}`)
  })

  // If we have some manual flowers for this month, return them
  if (manualFlowers.length > 0) {
    return manualFlowers
  }

  // Otherwise, generate flowers for this month
  return generateFlowersForMonth(year, month)
}

